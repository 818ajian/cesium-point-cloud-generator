package de.tum.gis.tiles3d.generator;

public class PntcGenerationException extends Exception {

	private static final long serialVersionUID = -4239046845332983864L;

	public PntcGenerationException() {
		super();
	}
	
	public PntcGenerationException(String message) {
		super(message);
	}
	
	public PntcGenerationException(Throwable cause) {
		super(cause);
	}
	
	public PntcGenerationException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
